/**
 * Returns a translation function for the given language.
 * Falls back to English if the key is missing in the target language.
 */
export function getTranslator(lang: string | null | undefined) {
  const locale = (translations as any)[lang || 'en'] || translations.en;
  return function t(key: string, fallback?: string): string {
    return locale[key] ?? translations.en[key] ?? fallback ?? key;
  };
}

/** Normalise a raw language value from DB/cookie to a valid Lang code */
export function normaliseLang(raw: string | null | undefined): Lang {
  const valid: Lang[] = ['en', 'fr', 'de', 'es', 'pt'];
  return valid.includes(raw as Lang) ? (raw as Lang) : 'en';
}
